#include<iostream>
using namespace std;

int main()
{
    int a = 2;

    cout << "Size of int is "  << sizeof(a)  << endl;
    cout << "Size of float is "<< sizeof(float)<< endl;
    cout << "Size of char is " << sizeof(char) << endl;
    cout << "Size of bool is " << sizeof(bool) << endl;

    return 0;
}